src = open('index.html', encoding='utf-8').read()

def block(tickets):
    items = []
    for t in tickets:
        lines = ['      {']
        keys = ['type', 'startDate', 'date', 'url']
        present = [k for k in keys if k in t]
        for j, k in enumerate(present):
            comma = ',' if j < len(present) - 1 else ''
            lines.append('        %s: %s%s' % (json.dumps(k, ensure_ascii=False), json.dumps(t[k], ensure_ascii=False), comma))
        lines.append('      }')
        items.append('\n'.join(lines))
    return '    "tickets": [\n' + ',\n'.join(items) + '\n    ],'

count = 0
for eid, tickets in T.items():
    pat = re.compile(r'("id": %d,.*?)(    "tickets": \[\n.*?\n    \],)' % eid, re.S)
    newblk = block(tickets)
    def rep(m):
        return m.group(1) + newblk
    src, n = pat.subn(rep, src, count=1)
    assert n == 1, 'id%d replaced=%d' % (eid, n)
    count += 1

open('index.html', 'w', encoding='utf-8').write(src)
print('ツアー%d件のtickets差替（会場別url付与）完了' % count)
